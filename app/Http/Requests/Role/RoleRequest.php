<?php
namespace App\Http\Requests\Role;


class RoleRequest
{
    // Default Property

    public $role_id;

    /**
     * @return mixed
     */
    public function getRoleId()
    {
        return $this->role_id;
    }

    /**
     * @param mixed $role_id
     */
    public function setRoleId($role_id)
    {
        $this->role_id = $role_id;
    }


}