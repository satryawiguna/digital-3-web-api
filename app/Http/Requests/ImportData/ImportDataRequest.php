<?php
namespace App\Http\Requests\ImportData;


class ImportDataRequest
{
    // Default Property

    public $file;

    /**
     * @return mixed
     */
    public function getFile()
    {
        return $this->file;
    }

    /**
     * @param mixed $file
     */
    public function setFile($file)
    {
        $this->file = $file;
    }

}